    @Nullable
    @Override
    public View onCreateView(LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        view = inflater.inflate(R.layout.comment_fragment, container, false);
        expandableListView = view.findViewById(R.id.elv_poetry_detail_comment);
        commentsList = generaTestData();
        initExapandableListView(commentsList);
        return view;
    }

    /**
     * 初始化评论和回复列表
     *
     * @param commentsList
     */
    private void initExapandableListView(final List<CommentDetailBean> commentsList) {
        expandableListView.setGroupIndicator(null);//取消分组的icon图标
        Log.d(TAG, "initExapandableListView: ListSize" + commentsList.size());
        //默认展开所有回复
        adapter = new CommentExpandAdapter(commentsList, getActivity().getApplicationContext());
        expandableListView.setAdapter(adapter);
        for (int i = 0; i < commentsList.size(); i++) {
            expandableListView.expandGroup(i);
        }
        expandableListView.setOnGroupClickListener(new ExpandableListView.OnGroupClickListener() {
            @Override
            public boolean onGroupClick(ExpandableListView expandableListView, View view, int i, long l) {
                boolean isExpanded = expandableListView.isGroupExpanded(i);
                if (isExpanded) {
                    expandableListView.collapseGroup(i);
                } else {
                    expandableListView.expandGroup(i, true);
                }
                showReplyDialog(i);
                return true;
            }
        });

        expandableListView.setOnChildClickListener(new ExpandableListView.OnChildClickListener() {
            @Override
            public boolean onChildClick(ExpandableListView expandableListView, View view, int i, int i1, long l) {
                Toast.makeText(getContext().getApplicationContext(), "点击了回复", Toast.LENGTH_SHORT).show();
                return false;
            }
        });

        expandableListView.setOnGroupExpandListener(new ExpandableListView.OnGroupExpandListener() {
            @Override
            public void onGroupExpand(int i) {

            }
        });

    }

